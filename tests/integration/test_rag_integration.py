    # ------------------------------------------------------------------
    # Test 6: Semantic retrieval relevance ordering
    # ------------------------------------------------------------------

    def test_semantic_retrieval_relevance_ordering(self, rag_components):
        """TC-INT-RAG06: Most semantically similar doc comes first in retrieve().

        StubEmbeddingProvider generates deterministic vectors from text hashes.
        The doc most similar to the query (same embedding as the query text) must
        rank first. We verify ordering by checking the first result contains
        the expected content.
        """
        embedder: StubEmbeddingProvider = rag_components["embedder"]
        retriever: SimpleRetriever = rag_components["retriever"]
        chunker: TextChunker = rag_components["chunker"]

        query_text = "collision avoidance rules"

        # Build and embed each document as a single chunk
        doc_similar = _make_doc(
            "sim-01", "COLREG rules for collision avoidance at sea", title="Similar"
        )
        doc_moderate = _make_doc(
            "mod-01", "Maritime safety regulations overview", title="Moderate"
        )
        doc_different = _make_doc(
            "dif-01", "Weather forecast for tomorrow in Seoul", title="Different"
        )

        for doc in [doc_similar, doc_moderate, doc_different]:
            chunks = chunker.chunk_document(doc)
            result = embedder.embed_texts([c.content for c in chunks])
            embedded_chunks = [
                DocumentChunk(
                    chunk_id=f"{doc.doc_id}_{i}",
                    doc_id=doc.doc_id,
                    content=c.content,
                    chunk_index=i,
                    start_char=c.start_char,
                    end_char=c.end_char,
                    embedding=vec,
                )
                for i, (c, vec) in enumerate(zip(chunks, result.vectors))
            ]
            retriever.add_chunks(embedded_chunks)

        query_embedding = embedder.embed_query(query_text)
        results = retriever.retrieve(query_embedding, top_k=3)

        assert len(results) > 0, "Should have at least one result"

        # Scores must be in descending order
        scores = [rc.score for rc in results]
        assert scores == sorted(scores, reverse=True), (
            f"Results not sorted by score descending: {scores}"
        )


    # Test 9: Retriever accepts chunks built manually from chunker
    # ------------------------------------------------------------------

    def test_retriever_accepts_pipeline_chunks(self):
        """TC-INT-RAG09: Manually chunk → embed → add to retriever, then retrieve works.

        Uses a dedicated retriever with similarity_threshold=-1.0 so that
        hash-based stub vectors with negative cosine similarity are not filtered.
        """
        config = EmbeddingConfig(dimension=64)
        embedder = StubEmbeddingProvider(config)
        # threshold=-1.0: accept all vectors including negatively-correlated ones
        retriever = SimpleRetriever(RAGConfig(top_k=5, similarity_threshold=-1.0))
        chunker = TextChunker(ChunkingConfig(chunk_size=200, chunk_overlap=20))

        doc = _make_doc("manual-doc-01", "COLREG rules govern vessel collision avoidance at sea.")
        raw_chunks = chunker.chunk_document(doc)

        # Embed and create new chunks with embeddings
        texts = [c.content for c in raw_chunks]
        embed_result = embedder.embed_texts(texts)

        embedded_chunks = [
            DocumentChunk(
                chunk_id=c.chunk_id,
                doc_id=c.doc_id,
                content=c.content,
                chunk_index=c.chunk_index,
                start_char=c.start_char,
                end_char=c.end_char,
                embedding=vec,
            )
            for c, vec in zip(raw_chunks, embed_result.vectors)
        ]
        added = retriever.add_chunks(embedded_chunks)
        assert added > 0, "At least one chunk should be indexed"

        query_vec = embedder.embed_query("collision avoidance")
        results = retriever.retrieve(query_vec)
        assert len(results) > 0, "retrieve() should return results from manually added chunks"


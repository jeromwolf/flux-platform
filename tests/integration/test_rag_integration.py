    # ------------------------------------------------------------------
    # Test 8: Chunker and embedder pipeline integration (multiple chunks)
    # ------------------------------------------------------------------

    def test_chunker_and_embedder_pipeline_integration(self, rag_components):
        """TC-INT-RAG08: Long text produces multiple chunks, each embedded and indexed.

        Uses '\n\n'-separated paragraphs so TextChunker (which splits on '\n\n')
        actually produces multiple chunks when chunk_size is set small enough.
        """
        embedder: StubEmbeddingProvider = rag_components["embedder"]
        retriever: SimpleRetriever = rag_components["retriever"]

        # chunk_size=50, separator='\n\n' — each paragraph is ~43 chars so
        # the chunker cannot merge them without exceeding chunk_size, yielding
        # one chunk per paragraph (6 chunks total).
        chunker = TextChunker(ChunkingConfig(chunk_size=50, chunk_overlap=10, separator="\n\n"))
        pipeline = DocumentPipeline(
            chunker=chunker, embedder=embedder, retriever=retriever
        )

        paragraphs = [
            "Maritime safety is critical for navigation.",
            "Vessels must follow COLREG rules at sea.",
            "Port authorities monitor vessel traffic closely.",
            "Weather conditions affect shipping schedules.",
            "Crews train regularly for emergency situations.",
            "Navigation charts are updated frequently.",
        ]
        long_text = "\n\n".join(paragraphs)
        doc = _make_doc("long-doc-01", long_text)
        result = pipeline.ingest_document(doc)

        assert result.success, f"Ingestion failed: {result.error}"
        assert result.chunks_created > 1, (
            f"Expected >1 chunks from paragraph-separated text with chunk_size=50, "
            f"got {result.chunks_created}"
        )
        assert result.chunks_embedded == result.chunks_created, (
            "All created chunks should be embedded"
        )
        assert result.chunks_indexed == result.chunks_embedded, (
            "All embedded chunks should be indexed in retriever"
        )
        assert retriever.chunk_count > 1, "Retriever should have multiple chunks indexed"



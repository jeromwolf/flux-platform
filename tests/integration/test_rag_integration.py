    def test_ingest_and_retrieve_semantic(self, rag_components):
        """TC-INT-RAG01: Ingest 3 docs via pipeline, then retrieve semantically.

        Verifies that RetrievedChunk objects are returned with score > 0
        when using query embeddings generated by StubEmbeddingProvider.
        """
        pipeline: DocumentPipeline = rag_components["pipeline"]
        embedder: StubEmbeddingProvider = rag_components["embedder"]
        retriever: SimpleRetriever = rag_components["retriever"]

        docs = [
            _make_doc("vessel-01", "Vessel KOREX departed Busan port heading to Incheon."),
            _make_doc("port-01", "Port of Busan handles 20 million TEU annually."),
            _make_doc("weather-01", "Weather warning issued for Korea Strait with strong winds."),
        ]

        for doc in docs:
            result = pipeline.ingest_document(doc)
            assert result.success, f"Ingestion failed for {doc.doc_id}: {result.error}"

        query_embedding = embedder.embed_query("vessel departure Busan")
        results = retriever.retrieve(query_embedding)

        assert len(results) > 0, "Expected at least one RetrievedChunk"
        for rc in results:
            assert isinstance(rc, RetrievedChunk)
            # StubEmbeddingProvider is hash-based so cosine scores can be negative.
            # The threshold is -1.0, so any returned score satisfies >= -1.0.
            assert rc.score >= -1.0, f"Score {rc.score} is below the minimum possible cosine value"
            assert rc.retrieval_mode == RetrievalMode.SEMANTIC


    # Test 13: HybridRAGEngine._reciprocal_rank_fusion combines both strategies
    # ------------------------------------------------------------------

    def test_hybrid_rrf_combines_both_strategies(self, rag_components):
        """TC-INT-RAG13: _reciprocal_rank_fusion merges semantic + keyword lists.

        Runs each strategy independently, then calls RRF directly and verifies
        the merged result contains chunk_ids from both source lists.
        """
        pipeline: DocumentPipeline = rag_components["pipeline"]
        embedder: StubEmbeddingProvider = rag_components["embedder"]
        retriever: SimpleRetriever = rag_components["retriever"]

        docs = [
            _make_doc("rrf-01", "Vessel speed limit regulations in Korean waters."),
            _make_doc("rrf-02", "Port congestion at Busan terminal during peak season."),
            _make_doc("rrf-03", "COLREG rule 13 covers overtaking vessels at sea."),
        ]
        for doc in docs:
            pipeline.ingest_document(doc)

        query_text = "vessel regulations"
        query_embedding = embedder.embed_query(query_text)

        semantic_list = retriever.retrieve(query_embedding, top_k=10)
        keyword_list = retriever.keyword_search(query_text, top_k=10)

        merged = HybridRAGEngine._reciprocal_rank_fusion(semantic_list, keyword_list, top_k=10)

        assert len(merged) > 0, "RRF should produce at least one result"

        merged_ids = {rc.chunk.chunk_id for rc in merged}
        semantic_ids = {rc.chunk.chunk_id for rc in semantic_list}
        keyword_ids = {rc.chunk.chunk_id for rc in keyword_list}
        combined_ids = semantic_ids | keyword_ids

        # All merged chunks must come from one of the source lists
        assert merged_ids.issubset(combined_ids), (
            "RRF result contains chunk_ids not present in either source list"
        )
        # Must have content from both when both lists are non-empty
        if semantic_list and keyword_list:
            assert len(merged) > 0


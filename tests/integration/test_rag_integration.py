    # ------------------------------------------------------------------
    # Test 11: Cosine similarity with stub embeddings (deterministic)
    # ------------------------------------------------------------------

    def test_cosine_similarity_with_stub_embeddings(self):
        """TC-INT-RAG11: Same text → identical vectors → cosine=1.0; different text → < 1.0."""
        embedder = StubEmbeddingProvider(EmbeddingConfig(dimension=64))

        vec_a1 = embedder.embed_query("collision avoidance")
        vec_a2 = embedder.embed_query("collision avoidance")  # identical text

        # Deterministic: same text must produce identical vectors
        assert vec_a1 == vec_a2, "StubEmbeddingProvider must be deterministic"

        sim_identical = SimpleRetriever.cosine_similarity(vec_a1, vec_a2)
        assert abs(sim_identical - 1.0) < 1e-9, (
            f"Cosine similarity of identical vectors should be 1.0, got {sim_identical}"
        )

        vec_b = embedder.embed_query("weather forecast Seoul tomorrow")
        sim_different = SimpleRetriever.cosine_similarity(vec_a1, vec_b)
        assert sim_different < 1.0, (
            "Cosine similarity of different texts should be < 1.0"
        )

